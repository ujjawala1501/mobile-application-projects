package com.anku.chhoti.hogwards;

import android.app.Activity;
import android.content.Intent;
import android.support.v7.app.AppCompatActivity;
import android.os.Bundle;
import android.view.View;
import android.widget.Button;
import android.widget.EditText;

public class RecordActivity extends Activity {
    EditText ed1,ed2;
    Button b;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_record);
        b=(Button)findViewById(R.id.button30);
        ed1=(EditText)findViewById(R.id.editText8);
        ed2=(EditText)findViewById(R.id.editText9);
        b.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                String s1= ed1.getText().toString();
                String s2=ed2.getText().toString();

                Intent ob=new Intent(RecordActivity.this,ShowActivity.class);
                ob.putExtra("Name",s1);
                ob.putExtra("Password",s2);
                startActivity(ob);
            }
        });
    }
}






























































































































































































































































































































































































































































































































































































































































































